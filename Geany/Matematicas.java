public class Matematicas {

	public static boolean esPar (int numero){
		
		return numero%2==0;
		
	}
	
	public static boolean esDivsiblePorTres (int numero){
		
		return numero%3==0;
		
	}
	
		public static boolean esDivsiblePorCinco (int numero){
		
		return numero%5==0;
		
	}
	
	public static boolean esDivsiblePorSeis (int numero){
		
		return numero%6==0;
		
	}
}

